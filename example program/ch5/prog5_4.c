/* prog5_4, 關係運算子的練習 */
#include <stdio.h>
#include <stdlib.h>
int main(void)
{
   if(5>2)	/* 判斷5>2是否成立 */
      printf("5>2成立\n");
   
   if(1)     	/* 1代表true，所以if的判斷結果會成立 */
      printf("此行一定會被執行\n");
      
   if(3==8) 	/* 判斷3是否等於8 */
      printf("3==8成立\n"); 
      
   system("pause");   
   return 0;
}

