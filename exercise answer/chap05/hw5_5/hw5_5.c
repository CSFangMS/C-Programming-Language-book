#include <stdio.h>
#include <stdlib.h>
int main(void)
{    
   printf("6%%4=%d\n",6%4);
   printf("12%%6=%d\n",12%6);
   printf("12%%12=%d\n",12%12);
   printf("35%%50=%d\n",35%50);
   printf("50%%35=%d\n",50%35);

   system("pause");
   return 0;
}

